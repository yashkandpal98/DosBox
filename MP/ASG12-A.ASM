.MODEL SMALL

.STACK 64

.DATA
	ONE DB 4
	TWO DB 8
	SUM DB "SUM: $"
	PROD DB "PRODUCT: $"
	DIFF DB "DIFFERENCE: $"
	QUO DB "QUOTIENT: $"
	

.CODE
MAIN PROC FAR
	
	MOV AX, @DATA
	MOV DS, AX
	
	LEA DI, PROD

   XXX: MOV DL, [DI]
	MOV AH, 02H
	INT 21H
	INC DI
	MOV CL, [DI]
	CMP CL, "$"
	JNE XXX
	
	MOV AL, ONE
	MOV BL, TWO
	MUL BL
	
	MOV CL, AH
	MOV AH, CL
	
	MOV BH, 00
	MOV BL, 0AH
	DIV BL
	
	MOV BL, AH
	MOV DL, AL
	ADD DL, 30H
	MOV AH, 02H
	INT 21H

	ADD BL, 30H
	MOV AL, BL
	MOV DL, AL
	MOV AH, 02H
	INT 21H

	MOV DL, 0AH
	INT 21H
	INT 0AH

	MOV DL, 0AH
	INT 21H
	INT 0AH

	LEA BX, SUM
	
   YYY: MOV DL, [BX]
	MOV AH, 02H
	INT 21H
	INC BX
	MOV CL, [BX]
	CMP CL, "$"
	JNE YYY

	MOV AH, 00
	MOV AL, ONE
	MOV BH, 00
	MOV BL, TWO
	
	ADD AL, BL
	
	MOV BL, 0AH
	DIV BL

	MOV BL, AH

	MOV DL, AL
	ADD DL, 30H
	MOV AH, 02H
	INT 21H
	
	ADD BL, 30H
	MOV DL, BL
	MOV AH, 02H
	INT 21H

	MOV DL, 0AH
	INT 21H
	INT 0AH

	MOV DL, 0AH
	INT 21H
	INT 0AH

	LEA SI, DIFF

   ZZZ: MOV DL, [SI]
	MOV AH, 02H
	INT 21H
	INC SI
	MOV CL, [SI]
	CMP CL, "$"
	JNE ZZZ

	MOV BL, ONE
	MOV AL, TWO

	SUB AL, BL

	MOV CL, TWO
	MOV DL, ONE
	CMP DL, CL
	JL ABC
	CMP DL, CL
	JG DEF

   ABC: MOV DL, '-'
	MOV AH, 02H
	INT 21H

   DEF: MOV AH, 00
	MOV AL, BL
	MOV BL, 0AH
	DIV BL

	MOV BL, AH

	ADD AL, 30H
	MOV DL, AL
	MOV AH, 02H
	INT 21H

	MOV AL, BL
	ADD AL, 30H
	MOV DL, AL
	MOV AH, 02H
	INT 21H

	MOV DL, 0AH
	INT 21H
	INT 0AH

	MOV DL, 0AH
	INT 21H
	INT 0AH

	LEA SI, QUO

   WWW: MOV DL, [SI]
	MOV AH, 02H
	INT 21H
	INC SI
	MOV CL, [SI]
	CMP CL, "$"
	JNE WWW

	MOV AH, 0
	MOV BH, 0
	MOV AL, ONE
	MOV BL, TWO
	DIV BL

	MOV BL, AH

	MOV DL, AL
	ADD DL, 30H
	MOV AH, 02H
	INT 21H

	MOV DL, '.'
	MOV AH, 02H
	INT 21H

	MOV AL, BL
	MOV CL, 0AH
	MUL CL
	MOV BL, TWO
	DIV BL

	MOV DL, AL
	ADD DL, 30H
	MOV AH, 02H
	INT 21H

	MOV AX, 4C00H
	INT 21H

MAIN ENDP
END MAIN