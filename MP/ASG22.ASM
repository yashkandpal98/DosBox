.STACK 64



.CODE


PRINTCORNER1 PROC NEAR
	MOV DL, 0C9H
	MOV AH, 02H
	INT 21H
	RET
PRINTCORNER1 ENDP

PRINTCORNER2 PROC NEAR
	MOV DL, 0BBH
	MOV AH, 02H
	INT 21H
	RET
PRINTCORNER2 ENDP

PRINTCORNER3 PROC NEAR
	MOV DL, 0C8H
	MOV AH, 02H
	INT 21H
	RET
PRINTCORNER3 ENDP

PRINTCORNER4 PROC NEAR
	MOV DL, 0BCH
	MOV AH, 02H
	INT 21H
	RET
PRINTCORNER4 ENDP

PRINTBORDER1 PROC NEAR
	MOV DL, 0BAH
	MOV AH, 02H
	INT 21H
	RET
PRINTBORDER1 ENDP

PRINTBORDER2 PROC NEAR
	MOV DL, 0CDH
	MOV AH, 02H
	INT 21H
	RET
PRINTBORDER2 ENDP

PRINTASC PROC NEAR
	MOV DL, CH
	MOV AH, 02H
	INT 21H
	RET
PRINTASC ENDP

CLRSCR PROC NEAR
	MOV AX, 0600H
	MOV BH, 07H
	MOV CX, 0000
	MOV DX, 184FH
	INT 10H
	RET
CLRSCR ENDP

SETCSRV PROC NEAR
	MOV AX, 0200H
	MOV BH, 00
	MOV DH, BL
	MOV DL, 00
	INT 10H
	RET
SETCSRV ENDP

SETCSRH PROC NEAR
	MOV AX, 0200H
	MOV BH, 00
	MOV DH, BL
	MOV DL, CL
	INT 10H
	RET
SETCSRH ENDP

SETEND PROC NEAR
	MOV AX, 0200H
	MOV BH, 00
	MOV DX, 1700H
	INT 10H
	RET
SETEND ENDP


MAIN PROC FAR
	CALL CLRSCR

	MOV CH, 00
	MOV BL, 00

	CALL SETCSRV
	CALL PRINTCORNER1

	MOV CL, 01H
   XXX: CALL SETCSRH
	CALL PRINTBORDER2
	MOV BX, 15000D
	ABC: DEC BX
	     JNZ ABC
	MOV BL, 00
	INC CL
	CMP CL, 4FH
	JL XXX

	CALL SETCSRH
	CALL PRINTCORNER2

   YYY: MOV BX, 30000D
	DEF: DEC BX
	     JNZ DEF

	MOV BL, DH
	INC BL

	
	CALL SETCSRV
	CALL PRINTBORDER1

	MOV CL, 01H
	JKL: CMP CH, 7FH
	     JG PQR
	MNO: CALL PRINTASC
	     INC CH
	     INC CL
	     CALL SETCSRH
	     CMP CL, 4FH
	     JL JKL

	PQR: CALL SETCSRH
	CALL PRINTBORDER1
	CMP DH, 16H
	JL YYY

	INC BL
	CALL SETCSRV
	CALL PRINTCORNER3

	MOV CL, 01H
   ZZZ: CALL SETCSRH
	CALL PRINTBORDER2
	MOV CH, BL
	MOV BX, 15000D
	STU: DEC BX
	     JNZ STU
	MOV BL, CH
	INC CL
	CMP CL, 4FH
	JL ZZZ

	CALL SETCSRH
	CALL PRINTCORNER4

	CALL SETEND

	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END MAIN