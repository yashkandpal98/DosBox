.MODEL SMALL

.STACK 64
.DATA
	ENT DB 'ENTER NO. OF ELEMENTS: $'
	ENT2 DB 'ENTER ELEMENTS: $'
	PRT DB 'LIST IS: $'

	A1 DB 3 DUP(0)
	A2 DB 3 DUP(0)

	A3 DB ?
.CODE

ENTERC PROC NEAR
	LEA SI, ENT

	MOV DL, [SI]
	MOV AH, 02H
   ABC: INT 21H
	INC SI
	MOV DL, [SI]
	CMP DL, '$'
	JNE ABC
	RET
ENTERC ENDP

ENTERE PROC NEAR
	LEA SI, ENT2

	MOV DL, [SI]
	MOV AH, 02H
   DEF: INT 21H
	INC SI
	MOV DL, [SI]
	CMP DL, '$'
	JNE DEF
	RET
ENTERE ENDP

PRINTE PROC NEAR
	MOV DL, AL
	MOV AH, 02H
	INT 21H
	RET
PRINTE ENDP

PRINTL PROC NEAR
	LEA SI, PRT
	MOV DL, [SI]
	MOV AH, 02H
   GHI: INT 21H
	INC SI
	MOV DL, [SI]
	CMP DL, '$'
	JNE GHI
	RET
PRINTL ENDP

LINE PROC NEAR
	MOV DL, 0AH
	INT 21H
	INT 0AH
	RET
LINE ENDP

MAIN PROC FAR

	MOV AX, @DATA
	MOV DS, AX

	MOV AH, 00
	INT 16H

	MOV CL, AL
	SUB CL, 30H
	MOV CH, CL
	CALL PRINTE

	CALL LINE
	CALL LINE

	CALL ENTERE

	CALL LINE

	LEA SI, A1
   XXX: MOV AH, 00
	INT 16H
	CALL PRINTE
	MOV [SI], AL
	INC SI
	CALL LINE
	DEC CL
	JNZ XXX

	CALL LINE
	CALL LINE

	CALL PRINTL

	MOV CL, CH

	LEA SI, A1
	MOV DL, [SI]
	MOV AH, 02H
   YYY: INT 21H
	MOV DL, ' '
	INT 21H
	INC SI
	MOV DL, [SI]
	DEC CL
	JNZ YYY

	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END MAIN