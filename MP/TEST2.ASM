.MODEL SMALL

.STACK 128
.DATA
	ENT DB 'ENTER NO. OF ELEMENTS: $'
	ENT2 DB 'ENTER ELEMENTS IN ARRAY 1: $'
	ENT3 DB 'ENTER ELEMENTS IN ARRAY 2: $'
	PRT DB 'ARRAY 1: $'
	PRT2 DB 'ARRAY 2: $'

	A1 DB 100 DUP(0)
	A2 DB 100 DUP(0)

	A3 DB ?
.CODE

ENTERC PROC NEAR
	LEA SI, ENT

	MOV DL, [SI]
	MOV AH, 02H
   ABC: INT 21H
	INC SI
	MOV DL, [SI]
	CMP DL, '$'
	JNE ABC
	RET
ENTERC ENDP

ENTERE PROC NEAR
	LEA SI, ENT2

	MOV DL, [SI]
	MOV AH, 02H
   DEF: INT 21H
	INC SI
	MOV DL, [SI]
	CMP DL, '$'
	JNE DEF
	RET
ENTERE ENDP

ENTERE2 PROC NEAR
	LEA SI, ENT3

	MOV DL, [SI]
	MOV AH, 02H
   GHI: INT 21H
	INC SI
	MOV DL, [SI]
	CMP DL, '$'
	JNE GHI
	RET
ENTERE2 ENDP

PRINTE PROC NEAR
	MOV DL, AL
	MOV AH, 02H
	INT 21H
	RET
PRINTE ENDP

PRINTL PROC NEAR
	LEA SI, PRT
	MOV DL, [SI]
	MOV AH, 02H
   JKL: INT 21H
	INC SI
	MOV DL, [SI]
	CMP DL, '$'
	JNE JKL
	RET
PRINTL ENDP

PRINTL2 PROC NEAR
	LEA SI, PRT2
	MOV DL, [SI]
	MOV AH, 02H
   MNO: INT 21H
	INC SI
	MOV DL, [SI]
	CMP DL, '$'
	JNE MNO
	RET
PRINTL2 ENDP

LINE PROC NEAR
	MOV DL, 0AH
	INT 21H
	INT 0AH
	RET
LINE ENDP

MAIN PROC FAR

	MOV AX, @DATA
	MOV DS, AX

	CALL ENTERC

	MOV AH, 00
	INT 16H

	MOV CL, AL
	SUB CL, 30H
	MOV CH, CL
	CALL PRINTE

	CALL LINE
	CALL LINE

	CALL ENTERE
	CALL LINE

	LEA SI, A1
   XXX: MOV AH, 00
	INT 16H
	CALL PRINTE
	MOV [SI], AL
	ADD SI, 3
	CALL LINE
	DEC CL
	JNZ XXX

	CALL LINE
	CALL LINE

	CALL ENTERE2
	CALL LINE

	MOV CL, CH
	LEA SI, A2
   YYY: MOV AH, 00
	INT 16H
	CALL PRINTE
	MOV [SI], AL
	CALL LINE
	ADD SI, 3
	DEC CL
	JNZ YYY

	CALL LINE
	CALL LINE

	CALL PRINTL

	MOV CL, CH

	LEA DI, A1
	MOV DL, [DI]
	MOV AH, 02H
   ZZZ: INT 21H
	MOV DL, ' '
	INT 21H
	ADD DI, 3
	MOV DL, [DI]
	DEC CL
	JNZ ZZZ

	CALL LINE

	CALL PRINTL2

	MOV CL, CH
	LEA BX, A2
	MOV DL, [BX]
	MOV AH, 02H
   WWW: INT 21H
	MOV DL, ' '
	INT 21H
	ADD BX, 3
	MOV DL, [BX]
	DEC CL
	JNZ WWW

	LEA SI, A1
	LEA DI, A2

	MOV CL, CH
	DEC CL

	CALL LINE
	CALL LINE

	INC SI
	INC DI

   XYZ: ADD DI, 2
	MOV AL, [SI]
	MOV [DI], AL
	SUB DI, 2
	ADD SI, 2
	MOV AL, [DI]
	MOV [SI], AL
	ADD DI, 3
	INC SI
	DEC CL
	JNZ XYZ

	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END MAIN