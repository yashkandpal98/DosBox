.MODEL SMALL

.STACK 64

.DATA
	ONE DW 4
	TWO DW 8
	SUM DB "SUM: $"
	PROD DB "PRODUCT: $"
	DIFF DB "DIFFERENCE: $"
	QUO DB "QUOTIENT: $"
	

.CODE
MAIN PROC FAR
	
	MOV AX, @DATA
	MOV DS, AX
	
	LEA DI, PROD

   XXX: MOV DL, [DI]
	MOV AH, 02H
	INT 21H
	INC DI
	MOV CL, [DI]
	CMP CL, "$"
	JNE XXX
	
	MOV AX, ONE
	MOV BX, TWO
	MUL BX
	
	MOV CL, AH
	MOV AH, CL
	
	MOV BH, 00
	MOV BL, 0AH
	DIV BL
	
	MOV BL, AH
	MOV DL, AL
	ADD DL, 30H
	MOV AH, 02H
	INT 21H

	ADD BL, 30H
	MOV AL, BL
	MOV DL, AL
	MOV AH, 02H
	INT 21H

	MOV DL, 0AH
	INT 21H
	INT 0AH

	MOV DL, 0AH
	INT 21H
	INT 0AH

	LEA BX, SUM
	
   YYY: MOV DL, [BX]
	MOV AH, 02H
	INT 21H
	INC BX
	MOV CL, [BX]
	CMP CL, "$"
	JNE YYY

	MOV AX, ONE
	MOV BX, TWO
	
	ADD AX, BX
	
	MOV BL, 0AH
	DIV BL

	MOV BL, AH

	MOV DL, AL
	ADD DL, 30H
	MOV AH, 02H
	INT 21H
	
	ADD BL, 30H
	MOV DL, BL
	MOV AH, 02H
	INT 21H

	MOV DL, 0AH
	INT 21H
	INT 0AH

	MOV DL, 0AH
	INT 21H
	INT 0AH

	LEA SI, DIFF

   ZZZ: MOV DL, [SI]
	MOV AH, 02H
	INT 21H
	INC SI
	MOV CL, [SI]
	CMP CL, "$"
	JNE ZZZ

	MOV BX, ONE
	MOV AX, TWO

	SUB AX, BX

	MOV CX, TWO
	MOV DX, ONE
	CMP CX, DX
	JL ABC
	CMP CX, DX
	JG DEF

   ABC: MOV DL, '-'
	MOV AH, 02H
	INT 21H

   DEF: MOV AH, 00
	MOV AL, BL
	MOV BL, 0AH
	DIV BL

	MOV BL, AH

	ADD AL, 30H
	MOV DL, AL
	MOV AH, 02H
	INT 21H

	MOV AL, BL
	ADD AL, 30H
	MOV DL, AL
	MOV AH, 02H
	INT 21H

	MOV DL, 0AH
	INT 21H
	INT 0AH

	MOV DL, 0AH
	INT 21H
	INT 0AH

	LEA SI, QUO

   WWW: MOV DL, [SI]
	MOV AH, 02H
	INT 21H
	INC SI
	MOV CL, [SI]
	CMP CL, "$"
	JNE WWW

	MOV DL, 30H
	MOV AH, 02H
	INT 21H

	MOV DL, '.'
	MOV AH, 02H
	INT 21H

	MOV AX, ONE
	MOV CX, 0AH
	MUL CX

	MOV CX, AX
	MOV AX, CX
	MOV BX, TWO
	DIV BX

	MOV BL, AH

	ADD AL, 30H
	MOV DL, AL
	MOV AH, 02H
	INT 21H

	ADD BL, 30H
	MOV DL, BL
	MOV AH, 02H
	INT 21H

	MOV AX, 4C00H
	INT 21H

MAIN ENDP
END MAIN